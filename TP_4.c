int	calcul_coeff(int *f, int u, int nn)
{
	int	i;
	int	res;

	res = 0;
	i = -1;
	while (++i < nn)
		res += ((f[i] + f[i ^ u]) % 2) ? -1 : 1;
	return (res);
}

void	Autocorrelation(int *r, int *f, int n)
{
	int	nn;
	int	i;

	nn = 1 << n;
	i = -1;
	while (++i < nn)
		r[i] = calcul_coeff(f, i, nn);
}

